package com.example.saiko.konseling;

public class Konseling {
    private int foto;
    private String name;
    private String kategori;
    private String lokasi;
    private String detail;
    private String waktuLayanan;
    private String nomorLayanan;
    private String daftarKonselor;
    private String website;


    public String getWebsite() {
        return website;
    }

    public void setWebsite(String website) {
        this.website = website;
    }


    public int getFoto() {
        return foto;
    }

    public void setFoto(int foto) {
        this.foto = foto;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getKategori() {
        return kategori;
    }

    public void setKategori(String kategori) {
        this.kategori = kategori;
    }

    public String getLokasi() {
        return lokasi;
    }

    public void setLokasi(String lokasi) {
        this.lokasi = lokasi;
    }

    public String getDetail() {
        return detail;
    }

    public void setDetail(String detail) {
        this.detail = detail;
    }

    public String getWaktuLayanan() {
        return waktuLayanan;
    }

    public void setWaktuLayanan(String waktuLayanan) {
        this.waktuLayanan = waktuLayanan;
    }

    public String getNomorLayanan() {
        return nomorLayanan;
    }

    public void setNomorLayanan(String nomorLayanan) {
        this.nomorLayanan = nomorLayanan;
    }

    public String getDaftarKonselor() {
        return daftarKonselor;
    }

    public void setDaftarKonselor(String daftarKonselor) {
        this.daftarKonselor = daftarKonselor;
    }
}
